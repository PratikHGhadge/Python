#Dictionaries
# unordered collection of data in key : value pair.

#how to creat dictionaries

user = {'name':'pratik','age':'18'}
print(user)
print(type(user))


user1 = dict(name = 'pratik',age = '18')
print(user1)
print(type(user1))
print(user1['age'])


# how to add data to empty dictionari
user_info = {}
user_info['name'] = 'pratik'
user_info['age'] = '18'
print(user_info)
